function AddIplToRequest(Request)
	table.insert(Map.Ipl.Request, Request)
end

function AddIplToRemove(Remove)
	table.insert(Map.Ipl.Remove, Remove)
end

function AddInterior(Interior)
	table.insert(Map.Interior, Interior)
end

Citizen.CreateThread(function()
	Citizen.Trace("[IPL]->Loading")
	LoadMpDlcMaps()
	UseFreemodeMapBehavior(true)
	Citizen.Trace("[IPL]->Requesting")
	for Index = 1, #Map.Ipl.Request do
		RequestIpl(Map.Ipl.Request[Index])
		--Citizen.Trace("[IPL]->["..Map.Ipl.Request[Index].."]->Loaded")
	end	
	Citizen.Trace("[IPL]->Removing")
	for Index = 1, #Map.Ipl.Remove do
		RemoveIpl(Map.Ipl.Remove[Index])
	end
	Citizen.Trace("[IPL]->Loaded")
	
	Citizen.Trace("[Interior]->Loading")
	for Index = 1, #Map.Interior do
		local Interior = Map.Interior[Index]
		for Coord = 1, #Interior.Coordinates do
			local Coordinates = Interior.Coordinates[Coord]
			local InteriorId = GetInteriorAtCoords(Coordinates.x, Coordinates.y, Coordinates.z)
			LoadInterior(InteriorId)
			for Prop = 1, #Interior.Props do
				Citizen.Wait(100)
				local CurrentProp = Interior.Props[Prop]
				EnableInteriorProp(InteriorId, CurrentProp)
				if Interior.Colours[CurrentProp] then
					SetInteriorPropColor(InteriorId, CurrentProp, Interior.Colours[CurrentProp])
				end
			end
			RefreshInterior(InteriorId)
		end
	end
	Citizen.Trace("[Interior]->Loaded")
end)